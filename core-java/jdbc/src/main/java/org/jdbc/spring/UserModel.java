package org.jdbc.spring;

public class UserModel 
{
	private int id;
	
	private String myName;

	public int getId() 
	{
		return id;
	}

	public void setId(int id) 
	{
		this.id = id;
	}

	public String getMyName() 
	{
		return myName;
	}

	public void setMyName(String myName) 
	{
		this.myName = myName;
	}
	
	
}
